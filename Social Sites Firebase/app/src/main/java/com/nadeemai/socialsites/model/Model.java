public class Model {

    private String title;
    private String desc;
    private String image;
    private String url;

    public Model(){}

    public String getTitle() {
        return title;
    }

    public String getDesc() {
        return desc;
    }

    public String getImage() {
        return image;
    }

    public String getUrl() {
        return url;
    }

}
